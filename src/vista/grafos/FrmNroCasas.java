    public void guardar() {
        casaDao.getCasa().setNumCasa(txtNroCasa.getText());
        try {
            casaDao.guardar();
        } catch (IOException ex) {
        }
        JOptionPane.showMessageDialog(null, "Se ha guardado");
        txtNroCasa.setText("");
        controlador.etiquetar();
        cargarCombos();
    }

    public void guardarRed() {

        if (cbxOrigen.getSelectedIndex() == cbxDestino.getSelectedIndex()) {
            JOptionPane.showMessageDialog(null, "No se puede conectar asi mismo");
        } else {
            redDao.getRed().setIdCasaOrigen(cbxOrigen.getSelectedIndex() + 1);
            redDao.getRed().setIdCasaDestino(cbxDestino.getSelectedIndex() + 1);
            redDao.getRed().setPeso(Double.valueOf(txtPeso.getText()));
            try {
                redDao.guardar();
            } catch (IOException ex) {
            }

            controlador.etiquetar();
            txtPeso.setText("");
        }
    }

    public void buscar() {
        if (cbxOrigenBus.getSelectedIndex() == cbxDestinoBus.getSelectedIndex()) {
            JOptionPane.showMessageDialog(null, "No se puede buscar a s√≠ mismo.");
        } else {
            try {
                long startTime = System.currentTimeMillis();

                ListaEnlazada<Integer> camino;
                String metodo;
                String tiempoMsg;

                if (cbxMetodo.getSelectedIndex() == 0) {
                    camino = controlador.getGrafo().floyd(cbxOrigenBus.getSelectedIndex() + 1, cbxDestinoBus.getSelectedIndex() + 1);
                    metodo = "Floyd-Warshall";
                } else {
                    camino = controlador.getGrafo().bellmanFord(cbxOrigenBus.getSelectedIndex() + 1, cbxDestinoBus.getSelectedIndex() + 1);
                    metodo = "Bellman-Ford";
                }

                long finalTime = System.currentTimeMillis() - startTime;

                if (camino.size() > 0) {
                    String casas = "Camino: [";

                    for (int i = 0; i < camino.size(); i++) {
                        casas = casas + controlador.getGrafo().getEtiqueta(camino.get(i)).getNumCasa();
                        if (i < camino.size() - 1) {
                            casas += " --> ";
                        }
                    }

                    casas += "]";
                    JOptionPane.showMessageDialog(null, casas);
                } else {
                    JOptionPane.showMessageDialog(null, "No hay camino entre el origen y el destino");
                }

                tiempoMsg = "Tiempo: " + finalTime + " ms";
                JOptionPane.showMessageDialog(null, "Metodo: " + metodo + "\n" + tiempoMsg);
            } catch (Exception ex) {
                Logger.getLogger(FrmNroCasas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

